//! Creating an array

let fruit=["Apple","Mango","Banana"]
let vegetables=["Beans","Cabbage","Lady Finger"]

//* Concat Method
console.log(fruit.concat(vegetables)) //! concats two arrays

//* indexOf Method
console.log(fruit.indexOf("Apple")) //! Returns the index of the specified item from the array

//* join Method
console.log(fruit.join()) //! Converts the array elements to a string.

//* Pop Method

console.log(vegetables.pop()) //! Deletes the last elements of an array
console.log(vegetables)

//* Reverse method

console.log(vegetables.reverse()) //!This method reverses the order of the array elements.

//* Sort Method
let numbers=[1,5,7,9,0,90] //!Sorts the array elements in a specified manner.
console.log(numbers.sort())

//* toString method
console.log(numbers.toString()) //! Converts the array elements to a string

//* valueOf method
console.log(numbers.valueOf()) //! returns the relevant Number Object holding the value of the argument passed



